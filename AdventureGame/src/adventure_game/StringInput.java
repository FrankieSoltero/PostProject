package adventure_game;

public class StringInput {
    private String input;
    /**
     * This is the constructor of StringInput and sets input to param
     * @param param of type String
     */
    public StringInput(String param) {
        this.input = param;
    }

    public void setStringInput(String string){
        this.input = string;
    }
    public String getStringInput(){
        return this.input;
    }
    
}
